#include<stdio.h>
int main() {
	
	float x=70, y=30;
	
	float add = x+y;
	float sub = x-y;
	float mul = x*y;
	float div = x/y;
	
	printf("Value of addition is: %f\n", add);
	printf("Value of Subtraction is: %f\n", sub);
	printf("Value of Multiplication is: %f\n", mul);
	printf("Value of division is: %f\n", div);
	
	return 0;
}
